package org.lejos.example;

public class LinjanArvo {
	
	private int valkoinen;
	private int musta;
	
	public LinjanArvo() {
		
	}
	
	public void setValkoinen(int arvo) {
		valkoinen = arvo;		
	}
	
	public int getValkoinen () {
		return valkoinen;
	}
	
	public void setMusta (int arvo) {
		musta = arvo;
	}
	
	public int getMusta () {
		return musta;
	}
}
